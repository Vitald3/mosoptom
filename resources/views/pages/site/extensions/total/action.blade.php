                action: '{{ $cost }}'
            },
            success: function(json) {
                if (json.success) {
                    $.get('{{ route(session('route_url') . '_checkout') }}', function(html) {
                        $('#totals').html($(html).find('#totals').html());
                    });
                }
            }
        });
    }
