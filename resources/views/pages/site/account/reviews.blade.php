@section('page-scripts')
    <script>
        $(document).ready(function(){
            $('.delete_review').on('click', function(){
                var self = $(this);
                var id = self.attr('data-id');

                $.ajax({
                    url: '{{ route('review_delete') }}',
                    dataType: 'json',
                    type: 'POST',
                    data: 'id=' + id,
                    success: function(json) {
                        if (json.success) {
                            if (self.closest('tr').length == 1) {
                                self.closest('.table').after('<p>{{ __('locale.text_reviews_empty') }}</p>');
                                self.closest('.table').remove();
                            } else {
                                self.closest('tr').remove();
                            }
                        }
                    }
                });

                return false;
            });
        });
    </script>
